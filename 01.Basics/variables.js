let name = "Yash";         //variable declared with let will get only block scope and functional scope. It is recommended to use to declare variables.
var age = "19";           //variable declared with var will get global scope. So try to avoid it as much as possible.
const dob = "08-08-2004"; //variable assigned with const will not be able to modify.
city = "Kadapa";

console.table([name, age, dob, city]);